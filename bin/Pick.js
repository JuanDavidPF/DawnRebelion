class Pick {

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    constructor(app, x, y) {
        this.app = app;
        this.x = x;
        this.y = y;
        this.selector = this.app.int(this.app.random(0, 3));
        if (this.selector == 0) this.tipo = "Suelo";
        if (this.selector == 1) this.tipo = "Mina";
        if (this.selector == 2) this.tipo = "Andamio";

        switch (this.tipo) {
            case "Suelo":
                this.hover = this.app.loadImage("./data/Escenario/Suelo/tierra.png");
                break;

            case "Mina":
                this.hover = this.app.loadImage("./data/Escenario/Armas/mina1.png");
                break;

            case "Andamio":
                this.hover = this.app.loadImage("./data/Escenario/Armas/andamio.png");
                break;

        } //cierra el switch de tipo
    } //cierra el constructor de pick
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    pintar() {

        this.app.imageMode(this.app.CENTER);
        this.app.image(this.hover, this.x, this.y);


    } //cierra el pintar





} //cierra la clase pick